
#! Arithmetic Operators
a = 9
b = 5
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a // b =", a // b)
print("a % b =", a % b)
print("a ** b =", a ** b)


#! Comparison Operators 
print("a == b :", a == b)
print("a != b :", a != b)
print("a > b :", a > b)
print("a < b :", a < b)
print("a >= b :", a >= b)
print("a <= b :", a <= b)



#! Logical Operators 
a = True
b = False
print("a and b :", a and b)
print("a or b :", a or b)
print("not a :", not a)