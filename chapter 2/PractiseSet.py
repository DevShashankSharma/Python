a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

print("The sum of {0} and {1} is {2}".format(a, b, a + b))
print("The remainder of {0} and {1} is {2}".format(a, b, a % b))
print("The comparison between {0} and {1} is {2}".format(a, b, a > b))
print("The average of {0} and {1} is {2}".format(a, b, (a + b) / 2))
print("The square of {0} is {1}".format(a, a ** 2))